// creating a function with parameters whichh return a vlues in string 
function city_Country(city, country) {
    return "".concat(city, ", ").concat(country);
}
// calling a function  and print  the returnd values:
console.log(city_Country("kharachi", "pakistan"));
console.log(city_Country("tokyo", "japan"));
console.log(city_Country("berlon", "Germany"));
